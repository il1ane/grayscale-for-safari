const grayscaleAliases = {
  en: 'en',
  fr: 'fr',
  es: 'es',
  ja: 'ja',
  de: 'de',
  zh: 'zh',
  'zh-cn': 'zh',
  'zh-hans': 'zh',
  pt: 'pt',
  'pt-pt': 'pt',
  'pt-br': 'pt',
  it: 'it'
};


const grayscaleLanguageFlags = {
  en: '🇺🇸',
  fr: '🇫🇷',
  es: '🇪🇸',
  ja: '🇯🇵',
  de: '🇩🇪',
  zh: '🇨🇳',
  pt: '🇵🇹',
  it: '🇮🇹'
};

function setLanguageDropdownOpen(isOpen) {
  const toggle = document.querySelector('.language-dropdown-toggle');
  const menu = document.querySelector('.language-dropdown-menu');
  if (!toggle || !menu) return;

  toggle.setAttribute('aria-expanded', String(isOpen));
  menu.classList.toggle('is-open', isOpen);
}

function normalizeGrayscaleLanguage(language) {
  if (!language) return 'en';
  const normalized = language.toLowerCase();
  return grayscaleAliases[normalized] || grayscaleAliases[normalized.split('-')[0]] || 'en';
}

function setMetaContent(selector, content) {
  const node = document.querySelector(selector);
  if (node) node.setAttribute('content', content);
}

function setGrayscaleLanguage(language) {
  const selected = normalizeGrayscaleLanguage(language);
  const copy = grayscaleTranslations[selected] || grayscaleTranslations.en;

  document.documentElement.lang = selected === 'zh' ? 'zh-Hans' : selected;
  document.title = copy.metaTitle;
  setMetaContent('meta[name="description"]', copy.metaDescription);
  setMetaContent('meta[property="og:title"]', copy.metaTitle);
  setMetaContent('meta[name="twitter:title"]', copy.metaTitle);
  setMetaContent('meta[property="og:description"]', copy.metaDescription);
  setMetaContent('meta[name="twitter:description"]', copy.metaDescription);

  document.querySelectorAll('[data-i18n]').forEach((node) => {
    node.textContent = copy[node.dataset.i18n];
  });

  document.querySelectorAll('[data-screenshot]').forEach((image) => {
    const index = String(image.dataset.screenshot).padStart(2, '0');
    image.src = `images/screenshots/${selected}/${index}.jpg`;
    image.alt = `${copy.screenshotAlt} ${Number(image.dataset.screenshot)}`;
  });

  document.querySelector('.language-current-flag').textContent = grayscaleLanguageFlags[selected] || grayscaleLanguageFlags.en;

  document.querySelectorAll('.language-option').forEach((button) => {
    button.setAttribute('aria-current', button.dataset.lang === selected ? 'true' : 'false');
  });

  localStorage.setItem('grayscale-page-language', selected);
}

function bootGrayscalePage() {
  const storedLanguage = localStorage.getItem('grayscale-page-language');
  const browserLanguage = navigator.language.toLowerCase();
  const initialLanguage = normalizeGrayscaleLanguage(storedLanguage || browserLanguage);

  const toggle = document.querySelector('.language-dropdown-toggle');
  if (toggle) {
    toggle.addEventListener('click', (event) => {
      event.stopPropagation();
      setLanguageDropdownOpen(toggle.getAttribute('aria-expanded') !== 'true');
    });
  }

  document.querySelectorAll('.language-option').forEach((button) => {
    button.addEventListener('click', () => {
      setGrayscaleLanguage(button.dataset.lang);
      setLanguageDropdownOpen(false);
    });
  });

  document.addEventListener('click', (event) => {
    if (!event.target.closest('.language-nav-item')) {
      setLanguageDropdownOpen(false);
    }
  });

  document.addEventListener('keydown', (event) => {
    if (event.key === 'Escape') {
      setLanguageDropdownOpen(false);
    }
  });

  setGrayscaleLanguage(initialLanguage);
}

bootGrayscalePage();
